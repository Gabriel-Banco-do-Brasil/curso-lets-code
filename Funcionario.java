
public class Funcionario {
    
    String cpf;
    String nome;
    String email;
    String telefone;
    String endereco;
    String cargo;
    double salario;

    public Funcionario(String cpf, String nome, String email, String telefone, String endereco, String cargo,
            double salario) {
        this.cpf = cpf;
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.endereco = endereco;
        this.cargo = cargo;
        this.salario = salario;
    }

}
